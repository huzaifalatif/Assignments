let favoritePizzas: string[] = ['Pepperoni', 'Fajita', 'BBQ Chicken'];

// Loop through the array and print a sentence for each pizza
for (let pizza of favoritePizzas) {
    console.log(`I like ${pizza} pizza.`);
}

